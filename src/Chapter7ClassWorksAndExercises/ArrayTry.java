public class ArrayTry {
    public static void main(String[] args) {
        String[] cars = {"Benze", "toyota", "peugeot", "mazda", "ford"};
        for (int i = 0; i < cars.length ; i++) {
            System.out.println(cars[i]);
        }
        System.out.println(cars.length);
    }

}
