package Chapter7ClassWorksAndExercises;

public class ArrayOfColours {
    public static void main(String[] args) {
        String [] colours = new String[7];
        colours[0] = "Black";
        colours[1] = "White";
        colours[2] = "red";
        colours[3] = "Blue";
        colours[4] = "Yellow";
        colours[5] = "Pink";
        colours[6] = "Purple";
        for (int index = 0; index < colours.length; index++) {
            System.out.println(colours[index]);

        }

    }
}
