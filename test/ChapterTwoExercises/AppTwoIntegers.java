package ChapterTwoExercises;

public class AppTwoIntegers {

    public int SquareOfNumber1(int num1) {
        return num1 * num1;
    }

    public int SquareOfNumber2(int num2) {
        return num2 * num2;
    }

    public int AddTwoNumbers(int num1, int num2) {
        return num1 + num2;
    }

    public int SubTwoNumbers(int num1, int num2) {
        return num1 - num2;
    }
}
